/** Base adult ticket price (USD) by theater format. */
export const TICKET_PRICES_USD: Record<Format, number> = {
  IMAX: 10,
  DOLBY: 7,
  DOLBY3D: 8.5,
};

/** Discount multipliers applied on top of the base price. */
export const TICKET_DISCOUNTS = {
  /** Children: 50% off. */
  child: 0.5,
  /** Seniors: 50% off. */
  senior: 0.5,
  /** Popular Monday: 50% off. */
  promo: 0.5,
} as const;

/** Promotional weekdays (0 = Sunday … 6 = Saturday). Monday is the popular day. */
export const PROMO_WEEKDAYS = [1] as const;

export type AudienceType = keyof typeof TICKET_DISCOUNTS | "adult";

/** Computes the final ticket price given format, audience type, and purchase date. */
export function computeTicketPrice(
  format: Format,
  audience: AudienceType,
  date: Date = new Date()
): number {
  const base = TICKET_PRICES_USD[format];
  const isPromoDay = (PROMO_WEEKDAYS as readonly number[]).includes(date.getDay());
  let multiplier = 1;
  if (audience !== "adult") multiplier = TICKET_DISCOUNTS[audience];
  if (isPromoDay) multiplier = Math.min(multiplier, TICKET_DISCOUNTS.promo);
  return Math.round(base * multiplier * 100) / 100;
}

