export type ShowtimeEntry = {
  format: Format;
  subtitled: boolean;
  time: string;
};

export type ShowtimeGroup = {
  venue: string;
  city: City;
  entries: ShowtimeEntry[];
};

/**
 * Canonical 5-showtime template per venue (when IMAX is supported):
 * IMAX → Dolby Digital → Dolby 3D → Dolby Digital SUB → Dolby 3D SUB.
 * If the venue has no IMAX, that slot is replaced with another Dolby Digital
 * so every venue always offers exactly 5 showtimes.
 */
const SHOWTIME_TIMES = ["14:00", "16:30", "18:45", "20:30", "22:00"] as const;

function buildEntries(supportsImax: boolean): ShowtimeEntry[] {
  const slots: Array<{ format: Format; subtitled: boolean }> = supportsImax
    ? [
        { format: "IMAX",    subtitled: false },
        { format: "DOLBY",   subtitled: false },
        { format: "DOLBY3D", subtitled: false },
        { format: "DOLBY",   subtitled: true  },
        { format: "DOLBY3D", subtitled: true  },
      ]
    : [
        { format: "DOLBY",   subtitled: false },
        { format: "DOLBY3D", subtitled: false },
        { format: "DOLBY",   subtitled: true  },
        { format: "DOLBY3D", subtitled: true  },
        { format: "DOLBY",   subtitled: false },
      ];
  return slots.map((s, i) => ({ ...s, time: SHOWTIME_TIMES[i] }));
}

/**
 * Showtimes for a movie. Each venue offers exactly 5 showtimes
 * mixing IMAX, Dolby Digital, and Dolby 3D (with subtitled variants).
 * Optionally filtered by city.
 */
export function getShowtimes(_movie: Movie, city?: City): ShowtimeGroup[] {
  const venues = city ? VENUES.filter((v) => v.city === city) : VENUES;
  return venues.map((v) => ({
    venue: v.name,
    city: v.city,
    entries: buildEntries(v.technologies.includes("IMAX")),
  }));
}

