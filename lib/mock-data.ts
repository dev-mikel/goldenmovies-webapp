export type Format = "IMAX" | "DOLBY" | "DOLBY3D";
export const FORMATS: Format[] = ["IMAX", "DOLBY", "DOLBY3D"];

const FORMAT_LABELS: Record<Format, string> = {
  IMAX: "IMAX",
  DOLBY: "Dolby Digital",
  DOLBY3D: "Dolby 3D",
};

/** Human-readable format label, with a SUB suffix when subtitled. */
export function formatLabel(f: Format, subtitled = false): string {
  const base = FORMAT_LABELS[f];
  return subtitled ? `${base} SUB` : base;
}

